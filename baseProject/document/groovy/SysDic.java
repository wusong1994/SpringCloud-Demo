package com.sample;


public class SysDic {

  private long dicId;
  private String type;
  private String code;
  private String name;
  private long sort;


  public long getDicId() {
    return dicId;
  }

  public void setDicId(long dicId) {
    this.dicId = dicId;
  }


  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }


  public String getCode() {
    return code;
  }

  public void setCode(String code) {
    this.code = code;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public long getSort() {
    return sort;
  }

  public void setSort(long sort) {
    this.sort = sort;
  }

}
